package factor;

public class IBOFactorValue {
	long factorNumber;
	String factorName;
	String valueWay;
	String valueObj;
	
	
	public long getFactorNumber() {
		return factorNumber;
	}
	public void setFactorNumber(long factorNumber) {
		this.factorNumber = factorNumber;
	}
	public String getFactorName() {
		return factorName;
	}
	public void setFactorName(String factorName) {
		this.factorName = factorName;
	}
	public String getValueWay() {
		return valueWay;
	}
	public void setValueWay(String valueWay) {
		this.valueWay = valueWay;
	}
	public String getValueObj() {
		return valueObj;
	}
	public void setValueObj(String valueObj) {
		this.valueObj = valueObj;
	}
	
}
